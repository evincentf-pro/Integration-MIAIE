        <h1 class="title" id="historique">1. L'HISTOIRE DU MINISTERE DE L'INTEGRATION DEPUIS 2005</h1>

        <p>
            <img src="images/blog/plateau.jpg" alt="" width="100%">
            <br><br>
            Le ministère de l'Intégration Africaine et des Ivoiriens de l'Exterieur(MIAIE) à pour mission fondamentale la mise en oeuvre de la politique ivoirienne en matière d'intégration ivoirienne et de gestion des ivoiriens de l'extérieur. A cet effet, il assure la coordination et la promotion des politiques sectorielles en matière d'intégration africaine en vue d'une meilleure cohérence des propositions nationales en liaison avec les ministères techniques intéressés.
        </p>

        <p>
            Il participe à la définition, à la mise en oeuvre, au suivi et à l'évaluation des programmes communautaires en vue d'un dévéloppement harmonisé et équilibré des memebres en relation avec les différents Ministère techniques concernés, notamment dans le cadre de l'Union Economique et Monétaires Ouest Africaine(UEMOA) de la Communauté Economique des Etats de l'Afrique de l'Ouest(CEDEAO) et de l'Union Africaine(UA). Enfin, il définit et assure le suivi de la Politique d'intégration de communautés africains vivants en Côte d'ivoire, de la politique d'immigration et d'émigration africaine en liaison avec le ministre de l'Intérieur.
        </p>

        <p>
            Depuis le 04 juin 2012 SEM. Ally COULIBALY préside au destiné du Ministère de l'Intégration Africaine et des ivoiriens de l'Extérieur avec un Cabinet qui joue le rôle de conseil, de coordination et et d'appui à toutes les structures internes et externes.
        </p>

        <p>
            Le cabinet du ministère est situé au 17ème étage de la Tour B à la Cité Administrative dans la Commune des affaires de la capitale économique, Abidjan-Plateau. Les directions et Services rattachés sont logés au Plateau(Immeuble Lamblin et à Cocody.
        </p>

