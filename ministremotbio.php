					</p>

					<p>
						Notre département s’est aussi engagé dans l’élaboration d’un Plan Stratégique de l’Intégration (PSI) pour les 5 années à venir. En cohérence avec le Plan National de Développement (PND) 2016-2020, le PSI dresse un bilan diagnostic de l’intégration africaine et de la place que joue notre pays dans ce processus. Il identifie les grands défis et expose les axes stratégiques prioritaires en matière d’intégration régionale.
					</p>

					<p>
						C’est dans cette dynamique que le MIAIE abrite la coordination nationale du programme de Partenariat intra-africain pour la Mobilité des Talents (TMP) couvrant douze (12) pays pilotes en Afrique, pour la phase 2014 -016. Le TMP a pour objectifs de stimuler la croissance et la compétitivité des entreprises par la mobilité de l’expertise régionale, de mener des actions pour combler les lacunes de développement des compétences nécessaires et d’améliorer la mobilité des talents / main d’œuvre intra-africaine. Nous tenons là, une piste dans la recherche de solutions contre la migration irrégulière en Europe.
					</p>

					<p>
						Comme nous le voyons, malheureusement de plus en plus souvent en direct sur nos télévisions, la migration irrégulière est devenue un fléau mondial et une préoccupation majeure pour les dirigeants africains. Des milliers de nos compatriotes perdent la vie en tentant l’aventure de la traversée de la méditerranée pour rejoindre l’Occident. Ces nombreuses pertes en vie humaine nous interpellent tous. Face à cette tragédie, notre ministère a pris des mesures pour prévenir les risques et autres dangers sur le périlleux chemin des migrants.
					</p>

					<p>
						Notre pays dispose d’un Centre d’Accueil, d’Orientation et d’Information des migrants qui mène sous notre tutelle des actions afin d’inviter les candidats au départ à privilégier la migration sûre, ordonnée et régulière.
					</p>

					<p>
						Avec le soutien de nos partenaires, notre Département a réalisé, dans le district d’Abidjan et dans plusieurs villes de l’intérieur du pays, une grande campagne de sensibilisation sur les risques de la migration irrégulière. De même, l’action déterminante de notre ministère a permis le retour au bercail de 800 de nos compatriotes résidant en Libye, au Gabon et en Angola.
					</p>

					<p>
						En le faisant, nous avons contribué à traduire dans les faits la vision du Chef de l’Etat qui veut que chaque ivoirien puisse bénéficier du soutien et de la protection du Gouvernement, où qu’il se trouve.
					</p>

